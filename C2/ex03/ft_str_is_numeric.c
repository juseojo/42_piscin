int	ft_str_is_numeric(char	*str)
{
	int	i;

	i = 0;
	if (!str[i])
		return (1);
	while (str[i])
	{
		if (!('0' <= str[i] && '9' >= str[i]))
			return (0);
		i++;
	}
	return (1);
}
